$(function(){
	$("#showfabricins").click(function(){
		bdc(this);
		$(".WordSection1").hide();
		$("#fabricins").show();
	})
	$("#showfabricdev").click(function(){
		bdc(this);
		$(".WordSection1").hide();
		$("#single").show();
	})
	$("#showca").click(function(){
		bdc(this);
		$(".WordSection1").hide();
		$("#ca").show();
	})
	$("#showapi").click(function(){
		bdc(this);
		$(".WordSection1").hide();
		$("#api").show();
	})
	$("#showsingle").click(function(event){
		event.stopPropagation();
		$(".WordSection1").hide();
		$("#single").show();
	})
	$("#showrestapi").click(function(event){
		event.stopPropagation();
		$(".WordSection1").hide();
		$("#restapi").show();
	})
	$("#showmulti").click(function(event){
		event.stopPropagation();
		$(".WordSection1").hide();
		$("#multi").show();
	})
	$("#showcontrol").click(function(event){
		event.stopPropagation();
		$(".WordSection1").hide();
		$("#control").show();
	})
})
function bdc(x){
	$(".nav").children("li").removeClass("active");
	$(x).addClass("active");
	$(x).siblings().children("ul").removeClass("in");
	$(x).children("ul").addClass("in");
}